# 두 수의 곱
def solution(num1, num2):
    answer = num1 * num2
    return answer

# 몫 구하기 
def solution(num1, num2):
    return num1 // num2

# 두 수의 차
def solution(num1, num2):
    return num1-num2

# 나머지 구하기
def solution(num1, num2):
    answer = num1 % num2
    return answer

# 나이 출력
def solution(age):
    answer = 2022-age+1
    return answer